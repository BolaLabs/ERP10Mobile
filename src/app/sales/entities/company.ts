
/**
 * Defines an Company.
 *
 * @export
 * @interface Company
 */
export interface Company {
    /**
    * The company Key. The key is unique to each company.
    *
    * @type {string}
    * @memberof Company
    */
    key: string;

    /**
     * The company Name.
     *
     * @type {string}
     * @memberof Company
     */
    name: string;
}
