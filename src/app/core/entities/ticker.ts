
/**
 * Defines a summary ticker.
 *
 * @export
 * @interface Ticker
 */
export interface Ticker {
    /**
     * The ticker title.
     *
     * @type {string}
     * @memberof Ticker
     */
    title: string;

    /**
     * The ticker html content.
     *
     * @type {HTMLElement}
     * @memberof Ticker
     */
    content: HTMLElement;
}
